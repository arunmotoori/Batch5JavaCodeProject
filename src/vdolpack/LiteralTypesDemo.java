package vdolpack;

public class LiteralTypesDemo {

	public static void main(String[] args) {
		
		byte a = 5; // Integer literal
		short b = 55; // Integer literal
		int c = 55555; // Integer literal
		long d = 5; // Integer literal
		long e = 5; // Integer literal
		long f = 55555555; // Integer literal
		long g = 55555555555555L; // Long literal
		long h = 5L; // Long literal
		long i = 55l; // Long literal
		float j = 5.5F; // Floating point literal
		double k = 5.5; // double literal
		double l = 5.5F; // floating point literal
		double m = 5.5d; // double literal
		double n = 5.5D; // dobule literal
		char o = 's'; // character literal
		char p = '$'; // character literal
		char q = '9'; // character literal
		boolean r = true; // boolean literal
		boolean s = false; // boolean literal
		String t = "Arun Motoori";  // String literal
		String u = "99"; //String literal
		
	}

}

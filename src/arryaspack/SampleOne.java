package arryaspack;

public class SampleOne {
	
	public static void main(String[] args) {
		
		Object[] ar = new Object[4]; 
		
		ar[0] = 9;
		ar[1] = 5.5;
		ar[2] = true;
		ar[3] = 's';
		
		System.out.println(ar[0]); // 9
		System.out.println(ar[1]); // 5
		System.out.println(ar[2]); // 7
		System.out.println(ar[3]);
	
	}

}
